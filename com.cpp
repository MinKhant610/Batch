#include <iostream>
using namespace std;

int main(){
    int a = 10;
    int b = 11;
    
    cout << (a != b) << endl; // equal
    cout << (a == b) << endl; // not equal
    cout << (a > b) << endl; // not equal
    cout << (a < b) << endl; // not equal
    cout << (a >= b) << endl; // not equal
    cout << (a <= b) << endl; // not equal

    return 0;
}